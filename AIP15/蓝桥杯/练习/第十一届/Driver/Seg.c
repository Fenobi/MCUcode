#include <Seg.h>

unsigned char seg_dula[] = {0xc0, 0xf9, 0xa4, 0xb0, 0x99, 0x92, 0x82, 0xf8, 0x80, 0x90, 0xff, 0xc6, 0x8c};
unsigned char Seg_Wela[] = {0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};

/* 
		函数名称：数码管显示函数
		函数说明：单个数码管显示数字
		入口参数：wela-需要显示的数码管 dula-需要显示的数字 point-使能小数点
		返回数据：无
*/
void Seg_Disp(unsigned char wela,dula,point)
{
	P0 = 0xff;
	P2 = P2 & 0x1f | 0xe0;
	P2 &= 0x1f;
	
	P0 = Seg_Wela[wela];
	P2 = P2 & 0x1f | 0xc0;
	P2 &= 0x1f;	
	
	P0 = Seg_Dula[dula];
	if(point)
		P0 &= 0x7f;
	P2 = P2 & 0x1f | 0xe0;
	P2 &= 0x1f;	
}