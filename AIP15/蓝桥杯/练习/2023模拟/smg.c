#include "smg.h"

u8 smg_num_arr[] = {0xc0, 0xf9, 0xa4, 0xb0, 0x99, 0x92, 0x82, 0xf8, 0x80, 0x90, 0xff, 0xc1};
u8 smg_addr_arr[] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80};

void smg_display(u8 addr, u8 num, u8 point)
{
    P0 = 0xff;
    P2 = P2 & 0x1f | 0xe0;
    P2 &= 0x1f;

    P0 = smg_addr_arr[addr];
    P2 = P2 & 0x1f | 0xc0;
    P2 &= 0x1f;

    P0 = smg_num_arr[num];
    if(point)
        P0 &= 0x7f;
    P2 = P2 & 0x1f | 0xe0;
    P2 &= 0x1f;
    
}